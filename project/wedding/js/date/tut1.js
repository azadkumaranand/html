// Date() 

let date = new Date();

console.log(date);

//getFullYear() get year as a four digit number

console.log(date.getFullYear());

//getMonth() get year as a four digit number(range: 0 to 11)

console.log(date.getMonth()+1);

//getDate() get day as a number (range: 1 to 31)

console.log(date.getDate());

//getDay() get weekday as a number (range: 0 to 6)
//0-> sunday and 6-> saturday
console.log(date.getDay());

//date time

//getHours() get hours(0 ot 23)

console.log(date.getHours());

//getMinutes() get hours(0 ot 59)

console.log(date.getMinutes());

//getSeconds() get hours(0 ot 59)

console.log(date.getSeconds());

//getMilliseconds() get hours(0 ot 59)

console.log(date.getMilliseconds());

//getTime() get hours(0 ot 59)

console.log(date.getTime());


