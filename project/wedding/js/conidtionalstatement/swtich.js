//switch statement

let a = 360;

switch(a){
    case 10:
        console.log("hello guys this is my 10 case");
        break;
    case 15:
        console.log("hello guys this is my 15 case");
        break;
    case 20:
        console.log("hello guys this is my 20 case"); 
        break;
    default:
        console.log("hello guys this is my default case");
}